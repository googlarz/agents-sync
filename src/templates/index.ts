function renderPython(corpus: RawCorpus, meta: ProjectMetadata, _projectPath: string): string {
  const { manifest, structure } = corpus;
  const name = meta.project.name;
  const fw = meta.project.framework ? ` / ${meta.project.framework}` : "";
  const testCmd = manifest.scripts?.test ?? "pytest";
  const deps = manifest.dependencies.slice(0, 12);

  const stackLines = [
    `- **Language**: Python`,
    meta.project.framework ? `- **Framework**: ${meta.project.framework}` : "",
    meta.stack.database ? `- **Database**: ${meta.stack.database}` : "",
    meta.stack.testing ? `- **Testing**: ${meta.stack.testing}` : "- **Testing**: pytest",
    meta.stack.deploy ? `- **Deploy**: ${meta.stack.deploy}` : "",
    deps.length ? `- **Key dependencies**: ${deps.join(", ")}` : "",
  ].filter(Boolean).join("\n");

  const dirs = structure.topLevelDirs.slice(0, 8);

  return `${renderHeader(`${name} — Python${fw} project.`)}

## Stack

${stackLines}

## Architecture

${dirs.length ? dirs.map((d) => `- \`${d}/\` — `).join("\n") : "- (run `agents-sync init .` with an API key to auto-detect directories)"}

## Conventions

- Follow PEP 8; use type hints everywhere
- Use \`pathlib.Path\` over \`os.path\`
- Prefer \`dataclasses\` or Pydantic models over raw dicts
- Tests in \`tests/\` directory, prefixed \`test_\`

## Testing

- **Run all tests**: \`${testCmd}\`
- **Coverage**: \`pytest --cov\`

## Gotchas

- (Add project-specific gotchas here as you discover them)

${renderBoundaries(meta)}
`;
}

