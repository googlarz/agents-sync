// ---------------------------------------------------------------------------
// Dispatch
// ---------------------------------------------------------------------------

function renderTemplate(
  template: TemplateName,
  corpus: RawCorpus,
  meta: ProjectMetadata,
  projectPath: string,
): string {
  switch (template) {
    case "typescript-node": return renderTypeScriptNode(corpus, meta, projectPath);
    case "python": return renderPython(corpus, meta, projectPath);
    case "go": return renderGo(corpus, meta, projectPath);
    case "rust": return renderRust(corpus, meta, projectPath);
    case "java": return renderJava(corpus, meta, projectPath);
    default: return renderGeneric(corpus, meta, projectPath);
  }
}
